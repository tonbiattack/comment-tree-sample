-- ============================================================
-- Materialized Path 方式: サブツリー取得
-- ============================================================
-- 目的:
--   指定したコメントのパス文字列をプレフィックスとして持つ全コメント
--   （自分自身を含む全子孫）を返す。
--
-- 想定ユースケース:
--   パス文字列の前方一致でスレッド部分木を高速取得する場面。
--   再帰クエリ不要で、LIKE インデックスを使った単純なフィルタで完結する。
--
-- 主要な出力項目:
--   id, post_id, parent_id, body, created_at
--
-- 実装方針:
--   Go 側（pathtree/repository.go）で事前に対象コメントの post_id と path を SELECT し、
--   その値をこのクエリのパラメータ（?）として渡す。
--
--   LIKE CONCAT(?, '%') の動作:
--     ? = '/1/2/' の場合、LIKE '/1/2/%' となり
--     path が '/1/2/' で始まる全てのコメントにマッチする。
--     - '/1/2/'   → コメント2自身（前方一致するため自己も含む）
--     - '/1/2/4/' → コメント4（子孫）
--
--   LIKE の落とし穴:
--     MySQL の LIKE は先頭にワイルドカードがある場合（LIKE '%foo'）インデックスが使えないが、
--     CONCAT(?, '%') のように末尾にのみ '%' を置く形式（前方一致）はインデックスが有効。
--     idx_comments_post_path (post_id, path(255)) が利用される。
--
--   隣接リスト（再帰 CTE）との比較:
--     - 再帰 CTE: ツリーの深さ分だけ反復実行
--     - Materialized Path: 常に O(サブツリーのノード数) の 1 回のスキャン
--
-- パラメータ:
--   1つ目の ? = サブツリーのルートとなるコメントの post_id
--   2つ目の ? = サブツリーのルートとなるコメントの path 文字列（例: '/1/2/'）
--              ※ id ではなく post_id と path を直接渡す（Go 側で事前取得している）
--
-- インデックス利用:
--   idx_comments_post_path (post_id, path(255)) または
--   path カラムへのプレフィックスインデックスが前方一致 LIKE に使われる。
--
-- 実行例（path = '/1/' の場合）:
--   LIKE '/1/%' にマッチする行:
--     path='/1/'（コメント1自身）, path='/1/2/', path='/1/3/', path='/1/2/4/'
--   ORDER BY depth → 0, 1, 1, 2 → id順: 1, 2, 3, 4
-- ============================================================
-- 指定した path を前方一致でたどり、サブツリー全体を取る
SELECT
    id,
    post_id,
    parent_id,
    body,
    created_at
FROM comments
-- post_id を先に絞ることで idx_comments_post_path (post_id, path(255)) を使いやすくする
WHERE post_id = ?
-- CONCAT(?, '%') で前方一致のパターンを生成する
-- 例: ? = '/1/2/' → path LIKE '/1/2/%'
  AND path LIKE CONCAT(?, '%')
-- depth 昇順（浅い階層から）→ 同深さは created_at → id で安定ソート
ORDER BY depth, created_at, id;
