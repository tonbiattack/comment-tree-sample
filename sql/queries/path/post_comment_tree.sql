-- ============================================================
-- Materialized Path 方式: 投稿全体のコメントツリー取得
-- ============================================================
-- 目的:
--   指定した投稿（post_id = ?）に属する全コメントを path 順に取得する。
--   Go 側の BuildTree に渡すことでメモリ上でツリー構造に変換する。
--
-- 想定ユースケース:
--   投稿詳細画面でコメントツリー全体を一度に描画する場面（Materialized Path 方式）。
--
-- 主要な出力項目:
--   id, post_id, parent_id, body, created_at
--
-- 実装方針:
--   Materialized Path の path カラムは辞書順に並べると
--   「親コメントが子コメントより必ず先に現れる」性質を持つ。
--
--   path の辞書順ソートがツリーの深さ優先に相当する理由:
--     '/1/'    < '/1/2/'  < '/1/2/4/' < '/1/3/'（文字列辞書順）
--     = コメント1 → コメント2 → コメント4 → コメント3
--     これは深さ優先探索（DFS）の訪問順序と同じになる。
--
--   BuildTree（Go 側）は入力順序に依存しないが、
--   path 順で渡すことで「親が子より先に並ぶ」ことが保証されるため
--   デバッグ時やログの可読性が向上する。
--
--   再帰 CTE を使わない理由:
--     path カラムがあれば ORDER BY path だけで
--     ツリー全体を正しい順序で取得できるため、再帰は不要。
--     これが Materialized Path の主要なメリットの一つ。
--
-- パラメータ:
--   ? = post_id (BIGINT)
--
-- インデックス利用:
--   idx_comments_post_path (post_id, path(255)) が
--   WHERE + ORDER BY の両方に使われる可能性がある。
--
-- 実行例（投稿1の場合）:
--   path 辞書順:
--     '/1/'（id=1, depth=0）
--     '/1/2/'（id=2, depth=1）
--     '/1/2/4/'（id=4, depth=2）
--     '/1/3/'（id=3, depth=1）
--   ※ ORDER BY path, created_at, id の順でソート
-- ============================================================
SELECT
    id,
    post_id,
    parent_id,
    body,
    created_at
FROM comments
WHERE post_id = ?
-- path 辞書順: 「/1/」→「/1/2/」→「/1/2/4/」→「/1/3/」のように
-- 親が子より必ず先に並ぶ（深さ優先順序）
ORDER BY path, created_at, id;
